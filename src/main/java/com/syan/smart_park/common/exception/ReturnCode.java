package com.syan.smart_park.common.exception;

public enum ReturnCode {

    // === 2xx 成功 ===
    RC200(200, "操作成功"),

    // === 4xx 客户端错误 ===
    RC400(400, "请求参数错误，请检查后重试"),
    RC401(401, "未授权，请先登录"),
    RC403(403, "禁止访问，权限不足"),
    RC404(404, "未找到请求的资源"),
    RC405(405, "请求方法不允许"),
    RC408(408, "请求超时"),
    RC409(409, "资源冲突，请检查数据"),
    RC410(410, "资源已不存在"),
    RC415(415, "不支持的媒体类型"),
    RC422(422, "请求格式正确，但语义错误"),
    RC429(429, "请求过于频繁，请稍后再试"),

    // === 5xx 服务器错误 ===
    RC500(500, "服务器内部错误，请稍后再试"),
    RC501(501, "功能未实现"),
    RC502(502, "网关错误"),
    RC503(503, "服务不可用"),
    RC504(504, "网关超时"),

    // === 业务相关状态码 (6xxx) ===
    // 用户相关
    RC600(600, "用户不存在"),
    RC601(601, "用户名或密码错误"),
    RC602(602, "用户已被禁用"),
    RC603(603, "用户已存在"),
    RC604(604, "密码强度不足"),
    RC605(605, "验证码错误"),
    RC606(606, "验证码已过期"),
    RC607(607, "账号或密码错误次数过多，请稍后再试"),
    RC608(608, "token已过期"),
    RC609(609, "token无效"),
    
    // 角色权限相关
    RC610(610, "角色不存在"),
    RC611(611, "角色编码已存在"),
    RC612(612, "角色正在使用中，无法删除"),
    RC613(613, "用户已拥有该角色"),
    RC614(614, "权限不存在"),
    RC615(615, "角色已拥有该权限"),

    // 停车场业务相关
    RC1000(1000, "停车场不存在"),
    RC1001(1001, "停车场已满"),
    RC1002(1002, "车位已被占用"),
    RC1003(1003, "车辆未入场"),
    RC1004(1004, "停车记录不存在"),
    RC1005(1005, "车牌号格式错误"),
    RC1006(1006, "预约时间冲突"),
    RC1007(1007, "预约已过期"),
    RC1008(1008, "支付金额不正确"),
    RC1009(1009, "支付失败"),
    RC1010(1010, "优惠券不可用"),
    RC1011(1011, "车辆已在场内"),

    // 订单相关
    RC1100(1100, "订单不存在"),
    RC1101(1101, "订单状态异常"),
    RC1102(1102, "订单已支付"),
    RC1103(1103, "订单已取消"),
    RC1104(1104, "退款申请失败"),

    // 文件相关
    RC1200(1200, "文件上传失败"),
    RC1201(1201, "文件类型不支持"),
    RC1202(1202, "文件大小超限"),
    RC1203(1203, "文件不存在"),

    // 数据相关
    RC1300(1300, "数据不存在"),
    RC1301(1301, "数据重复"),
    RC1302(1302, "数据格式错误"),
    RC1303(1303, "数据校验失败"),

    // 系统相关
    RC1400(1400, "系统配置错误"),
    RC1401(1401, "功能维护中"),
    RC1402(1402, "版本过低，请升级"),
    RC1403(1403, "API调用次数超限");

    private final int code;
    private final String message;

    ReturnCode(int code, String message) {
        this.code = code;
        this.message = message;
    }

    public int getCode() {
        return code;
    }

    public String getMessage() {
        return message;
    }

    /**
     * 根据code获取枚举
     */
    public static ReturnCode getByCode(int code) {
        for (ReturnCode returnCode : values()) {
            if (returnCode.getCode() == code) {
                return returnCode;
            }
        }
        return RC500;
    }

    /**
     * 判断code是否存在
     */
    public static boolean contains(int code) {
        for (ReturnCode returnCode : values()) {
            if (returnCode.getCode() == code) {
                return true;
            }
        }
        return false;
    }
}
